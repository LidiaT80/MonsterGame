
public class GameArea {
    private int width;
    private int height;
    private int startX;
    private int startY;


    public GameArea(){

        width=80;
        height=25;
        startX=9;
        startY=3;

    }

    public int getHeight() {
        return height;
    }

    public int getWidth() {
        return width;
    }

    public int getStartX() {
        return startX;
    }

    public int getStartY() {
        return startY;
    }



}
